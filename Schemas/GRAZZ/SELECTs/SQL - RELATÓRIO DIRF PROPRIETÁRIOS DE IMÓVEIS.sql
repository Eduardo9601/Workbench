SELECT --ORDEM,
       COD_UNIDADE_SHOW AS COD_UNIDADE,
       DTA_REFERENCIA,
       NUM_CPF_SHOW AS NUM_CPF,
       DES_PESSOA,       
       VAL_TOTAL,
       DESCONTO_SIMPLIFICADO,
       BASE_TRIBUTAVEL,
       VAL_IRRF
FROM (
    SELECT ORDEM,
           1 AS GRUPO_ORDEM,
           DTA_VCTO AS DTA_REFERENCIA,
           NUM_CPF AS NUM_CPF_ORDEM,
           NUM_CPF AS NUM_CPF_SHOW,
           DES_PESSOA,
           COD_UNIDADE AS COD_UNIDADE_ORDEM,
           COD_UNIDADE AS COD_UNIDADE_SHOW,
           TO_CHAR(NVL(VAL_TOTAL, 0), 'FM999G999G990D00') AS VAL_TOTAL,
           TO_CHAR(NVL(DESCONTO_SIMPLIFICADO, 0), 'FM999G999G990D00') AS DESCONTO_SIMPLIFICADO,
           TO_CHAR(NVL(VAL_TOTAL, 0) - NVL(DESCONTO_SIMPLIFICADO, 0), 'FM999G999G990D00') AS BASE_TRIBUTAVEL,
           TO_CHAR(NVL(VAL_IRRF, 0), 'FM999G999G990D00') AS VAL_IRRF,
           TO_CHAR(NVL(VAL_LIQUIDO, 0), 'FM999G999G990D00') AS VAL_LIQUIDO
    FROM DADOS_IRRF

    UNION ALL

    SELECT ORDEM,
           1 AS GRUPO_ORDEM,
           NULL AS DTA_REFERENCIA,
           NUM_CPF AS NUM_CPF_ORDEM,
           NULL AS NUM_CPF_SHOW,
           NULL AS DES_PESSOA,
           COD_UNIDADE AS COD_UNIDADE_ORDEM,
           NULL AS COD_UNIDADE_SHOW,
           TO_CHAR(NVL(TOT_VAL_TOTAL, 0), 'FM999G999G990D00') AS VAL_TOTAL,
           TO_CHAR(NVL(TOT_DESCONTO_SIMPLIFICADO, 0), 'FM999G999G990D00') AS DESCONTO_SIMPLIFICADO,
           TO_CHAR(NVL(TOT_VAL_TOTAL, 0) - NVL(TOT_DESCONTO_SIMPLIFICADO, 0), 'FM999G999G990D00') AS BASE_TRIBUTAVEL,
           TO_CHAR(NVL(TOT_VAL_IRRF, 0), 'FM999G999G990D00') AS VAL_IRRF,
           TO_CHAR(NVL(TOT_VAL_LIQUIDO, 0), 'FM999G999G990D00') AS VAL_LIQUIDO
    FROM TOTAIS_CPF

    /*UNION ALL

    SELECT ORDEM,
           2 AS GRUPO_ORDEM,
