           TO_CHAR(NVL(TOT_VAL_IRRF, 0), 'FM999G999G990D00') AS VAL_IRRF,
           TO_CHAR(NVL(TOT_VAL_LIQUIDO, 0), 'FM999G999G990D00') AS VAL_LIQUIDO
    FROM TOTAIS_UNIDADE*/
)
ORDER BY NUM_CPF_ORDEM NULLS LAST,
         COD_UNIDADE_ORDEM,
         GRUPO_ORDEM,         
         ORDEM,
         DTA_REFERENCIA
