        B.COD_VINCULO_EMPREG,
        CE.DATA_INI_EXP,
        CE.DATA_FIM_EXP,
        CE.DIAS_CONTRATO,
        CE.DATA_AVALIACAO,
        CE.IND_AVALIACAO,
        CE.NUM_DIAS,
        CE.IND_AVALIACOES,
        CE.NOME_TIPO_CONTRATO,
        CE.DURACAO_CONTRATO,
        SA.COD_AFAST,
        SA.DES_AFAST,
        SA.DES_STATUS_AFAST,
        SA.DATA_INI_AFAST,
        SA.DATA_FIM_AFAST,
        ROW_NUMBER() OVER (
            PARTITION BY B.COD_ORGANOGRAMA, B.COD_UNIDADE
            ORDER BY B.DES_PESSOA, B.COD_CONTRATO
        ) AS ORDEM_PESSOA
    FROM BASE_APRENDIZ B
    CROSS JOIN PARAMS P
    LEFT JOIN CONTRATO_EXPERIENCIA CE
           ON CE.COD_CONTRATO = B.COD_CONTRATO
          AND CE.RN = 1
    LEFT JOIN STATUS_AFASTADOS SA
           ON SA.COD_CONTRATO = B.COD_CONTRATO
    WHERE B.DATA_ADMISSAO <= P.DATA_FIM
      AND (B.DATA_DEMISSAO IS NULL OR B.DATA_DEMISSAO > P.DATA_FIM)
),

APRENDIZES_DEMITIDOS_PERIODO AS (
    SELECT
        B.DES_STATUS,
        B.COD_UNIDADE,
        B.DES_UNIDADE,
        B.COD_ORGANOGRAMA,
        B.COD_REDE,
        B.DES_REDE,
        B.COD_TIPO,
        B.DES_TIPO,
        2 AS COD_STATUS_REL,
        'DEMITIDO' AS STATUS_REL,
        B.COD_CONTRATO,
        B.DES_PESSOA,
        B.DATA_ADMISSAO,
        B.DATA_DEMISSAO,
        B.COD_VINCULO_EMPREG,
        ROW_NUMBER() OVER (
            PARTITION BY B.COD_ORGANOGRAMA, B.COD_UNIDADE
            ORDER BY B.DATA_DEMISSAO, B.DES_PESSOA, B.COD_CONTRATO
        ) AS ORDEM_PESSOA
    FROM BASE_APRENDIZ B
    CROSS JOIN PARAMS P
    WHERE B.DATA_DEMISSAO BETWEEN P.DATA_INICIO AND P.DATA_FIM
),



DETALHE_RELATORIO AS (
    /* ATIVOS */
    SELECT
        A.DES_STATUS,
        A.COD_ORGANOGRAMA,
        A.COD_UNIDADE,
        A.DES_UNIDADE AS UNIDADE,
        A.COD_REDE,
        A.DES_REDE AS REDE,
        A.COD_TIPO,
        A.DES_TIPO AS TIPO,

        A.COD_STATUS_REL,
        A.STATUS_REL,
        A.COD_CONTRATO,
        A.DES_PESSOA,
        A.COD_CONTRATO || ' - ' || A.DES_PESSOA AS APRENDIZ,
        A.DATA_ADMISSAO AS DT_ADMISSAO,
        A.DATA_DEMISSAO AS DT_DEMISSAO,
        A.COD_VINCULO_EMPREG AS VINCULO,
        A.DATA_INI_EXP AS DT_INI_CONTRATO_EXP,
        A.DATA_FIM_EXP AS DT_FIM_CONTRATO_EXP,
        A.DIAS_CONTRATO,
        A.NOME_TIPO_CONTRATO,
        A.DURACAO_CONTRATO,
        A.COD_AFAST,
        A.DES_AFAST,
        A.DES_STATUS_AFAST AS STATUS_AFAST,
        A.DATA_INI_AFAST AS DT_INI_AFAST,
        A.DATA_FIM_AFAST AS DT_FIM_AFAST,
        A.ORDEM_PESSOA
    FROM APRENDIZES_ATIVOS A
    WHERE (A.COD_TIPO = :TIPO OR :TIPO = 0)
      AND (A.COD_UNIDADE = :UNIDADE OR :UNIDADE = 0)
      AND (A.COD_REDE = :REDE OR :REDE = 0)

    UNION ALL

    /* DEMITIDOS */
    SELECT
        D.DES_STATUS,
        D.COD_ORGANOGRAMA,
        D.COD_UNIDADE,
        D.DES_UNIDADE AS UNIDADE,
        D.COD_REDE,
        D.DES_REDE AS REDE,
        D.COD_TIPO,
        D.DES_TIPO AS TIPO,

        D.COD_STATUS_REL,
        D.STATUS_REL,
        D.COD_CONTRATO,
        D.DES_PESSOA,
        D.COD_CONTRATO || ' - ' || D.DES_PESSOA AS APRENDIZ,
        D.DATA_ADMISSAO AS DT_ADMISSAO,
        D.DATA_DEMISSAO AS DT_DEMISSAO,
        D.COD_VINCULO_EMPREG AS VINCULO,
        CAST(NULL AS DATE) AS DT_INI_CONTRATO_EXP,
        CAST(NULL AS DATE) AS DT_FIM_CONTRATO_EXP,
        CAST(NULL AS NUMBER) AS DIAS_CONTRATO,
        CAST(NULL AS VARCHAR2(200)) AS NOME_TIPO_CONTRATO,
        CAST(NULL AS NUMBER) AS DURACAO_CONTRATO,
        CAST(NULL AS NUMBER) AS COD_AFAST,
        CAST(NULL AS VARCHAR2(200)) AS DES_AFAST,
        CAST(NULL AS VARCHAR2(200)) AS STATUS_AFAST,
        CAST(NULL AS DATE) AS DT_INI_AFAST,
        CAST(NULL AS DATE) AS DT_FIM_AFAST,
        D.ORDEM_PESSOA
    FROM APRENDIZES_DEMITIDOS_PERIODO D
    WHERE (D.COD_TIPO = :TIPO OR :TIPO = 0)
      AND (D.COD_UNIDADE = :UNIDADE OR :UNIDADE = 0)
      AND (D.COD_REDE = :REDE OR :REDE = 0)
)

