AGG_I AS (
  SELECT
    PERIODO, ANO, NIVEL_KEY,
    COUNT(*) AS TOTAL,
    SUM(CASE WHEN SEXO='F' THEN 1 ELSE 0 END) AS FEM_QTDE,
    SUM(CASE WHEN SEXO='M' THEN 1 ELSE 0 END) AS MASC_QTDE
  FROM CLASSIF_B12
  GROUP BY PERIODO, ANO, NIVEL_KEY
),

GRID_I AS (
  SELECT
    p.PERIODO, p.ANO,
    n.ORDEM, n.NIVEL_KEY, n.NIVEL_DESC,
    NVL(a.FEM_QTDE,0) AS FEM_QTDE,
    ROUND(100*NVL(a.FEM_QTDE,0)/NULLIF(NVL(a.TOTAL,0),0),0) AS FEM_PCT,
    NVL(a.MASC_QTDE,0) AS MASC_QTDE,
    ROUND(100*NVL(a.MASC_QTDE,0)/NULLIF(NVL(a.TOTAL,0),0),0) AS MASC_PCT,
    NVL(a.TOTAL,0) AS TOTAL
  FROM PARAMS p
  CROSS JOIN NIVEIS n
  LEFT JOIN AGG_I a
    ON a.PERIODO=p.PERIODO AND a.ANO=p.ANO AND a.NIVEL_KEY=n.NIVEL_KEY
),

TOTAIS_I AS (
  SELECT
    PERIODO,
    ANO,
    COUNT(CASE WHEN SEXO='F' THEN 1 END) AS TOT_FEM_QTDE,
    
    ROUND(100 * COUNT(CASE WHEN SEXO='F' THEN 1 END) /
          NULLIF(COUNT(*),0),0) AS TOT_FEM_PCT,

    COUNT(CASE WHEN SEXO='M' THEN 1 END) AS TOT_MASC_QTDE,
    ROUND(100 * COUNT(CASE WHEN SEXO='M' THEN 1 END) /
          NULLIF(COUNT(*),0),0) AS TOT_MASC_PCT,

    COUNT(*) AS TOT_GERAL_QTDE,

    ROUND(100 * (
      COUNT(CASE WHEN SEXO='F' THEN 1 END) +
      COUNT(CASE WHEN SEXO='M' THEN 1 END)
    ) / NULLIF(COUNT(*),0),0) AS TOT_GERAL_PCT

  FROM CLASSIF
  WHERE NIVEL_KEY <> 'DIR_ESTAT_FIN'
  GROUP BY PERIODO, ANO
),


