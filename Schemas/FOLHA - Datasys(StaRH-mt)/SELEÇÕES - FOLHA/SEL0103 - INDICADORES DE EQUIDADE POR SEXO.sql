CT_BASE AS (
  SELECT
    p.PERIODO,
    p.ANO,
    p.DT_REF,
    ct.COD_CONTRATO,
    ct.SEXO,
    ROW_NUMBER() OVER (
      PARTITION BY p.PERIODO, p.ANO, ct.COD_CONTRATO
      ORDER BY NVL(ct.DATA_DEMISSAO, DATE '9999-12-31') DESC,
               ct.DATA_ADMISSAO DESC
    ) AS RN
  FROM PARAMS p
  JOIN V_DADOS_CONTRATO_AVT ct
    ON 1=1
  WHERE p.DT_REF BETWEEN ct.DATA_ADMISSAO
                     AND NVL(ct.DATA_DEMISSAO, DATE '9999-12-31')
),

