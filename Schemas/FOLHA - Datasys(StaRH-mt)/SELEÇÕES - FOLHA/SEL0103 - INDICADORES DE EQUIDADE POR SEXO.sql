    ROUND(100 * SUM(CASE WHEN SEXO = 'F' THEN 1 ELSE 0 END) / NULLIF(COUNT(*),0), 0) AS B2_TOT_FEM_PCT,
    SUM(CASE WHEN SEXO = 'M' THEN 1 ELSE 0 END) AS B2_TOT_MASC_QTDE,
    ROUND(100 * SUM(CASE WHEN SEXO = 'M' THEN 1 ELSE 0 END) / NULLIF(COUNT(*),0), 0) AS B2_TOT_MASC_PCT,
    COUNT(*) AS B2_TOT_GERAL_QTDE,
    ROUND(100 * (
      SUM(CASE WHEN SEXO = 'F' THEN 1 ELSE 0 END) +
      SUM(CASE WHEN SEXO = 'M' THEN 1 ELSE 0 END)
    ) / NULLIF(COUNT(*),0), 0) AS B2_TOT_GERAL_PCT
  FROM CLASSIF
  WHERE NIVEL_KEY IN ('CONSELHO_ADM','CONSELHO_FISC','DIR_ESTAT_VAR','DIR_ESTAT_FIN')
    AND NIVEL_KEY <> 'DIR_ESTAT_FIN'
  GROUP BY PERIODO, ANO
