       R.AVISO,
       R.DIAS_TOTAL,
       R.TRABALHADOS,
       R.DISPENSADOS,
       R.FUNCIONARIO,
       R.EMPRESA,
       R1.DATA_RESCISAO_COMPL,
       R1.DATA_COMPLEMENTO_COMPL,
       R1.DATA_PAGAMENTO_COMPL,
       E.HOR_1_ENT,
       E.HOR_1_SAI,
       E.HOR_2_ENT,
