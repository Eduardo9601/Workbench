       G.INFO_HOR_8,
       B.SALARIO,
       C.VALOR_ANTE,
       F.NUM_DIAS AS DIAS_CONT_EXP,
       F.DATA_INI_EXP,
       F.DATA_FIM_EXP,

       /*CASE
