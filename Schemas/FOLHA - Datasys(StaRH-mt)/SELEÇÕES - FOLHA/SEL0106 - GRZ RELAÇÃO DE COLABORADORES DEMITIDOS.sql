       CASE
           WHEN D.DEPENDENTE_1 IS NOT NULL THEN
            INITCAP(D.DEPENDENTE_1) || ' (' || INITCAP(D.GRAU_PARENT_1) || ')'
           ELSE
            NULL
       END AS DEPENDENTE_1,
       CASE
           WHEN D.DEPENDENTE_2 IS NOT NULL THEN
            INITCAP(D.DEPENDENTE_2) || ' (' || INITCAP(D.GRAU_PARENT_2) || ')'
           ELSE
            NULL
       END AS DEPENDENTE_2,
       CASE
           WHEN D.DEPENDENTE_3 IS NOT NULL THEN
            INITCAP(D.DEPENDENTE_3) || ' (' || INITCAP(D.GRAU_PARENT_3) || ')'
           ELSE
            NULL
       END AS DEPENDENTE_3,
       CASE
           WHEN D.DEPENDENTE_4 IS NOT NULL THEN
            INITCAP(D.DEPENDENTE_4) || ' (' || INITCAP(D.GRAU_PARENT_4) || ')'
           ELSE
            NULL
       END AS DEPENDENTE_4,
       CASE
           WHEN D.DEPENDENTE_5 IS NOT NULL THEN
            INITCAP(D.DEPENDENTE_5) || ' (' || INITCAP(D.GRAU_PARENT_5) || ')'
           ELSE
            NULL
       END AS DEPENDENTE_5,
       CASE
           WHEN D.DEPENDENTE_6 IS NOT NULL THEN
            INITCAP(D.DEPENDENTE_6) || ' (' || INITCAP(D.GRAU_PARENT_6) || ')'
           ELSE
            NULL
       END AS DEPENDENTE_6,
       CASE
           WHEN D.DEPENDENTE_7 IS NOT NULL THEN
            INITCAP(D.DEPENDENTE_7) || ' (' || INITCAP(D.GRAU_PARENT_7) || ')'
           ELSE
            NULL
       END AS DEPENDENTE_7,

       (SELECT QTD_TOTAL FROM QTD_CONTRATOS_DISTINCT) AS QTD_CONTRATOS,
       'De ' || :DEMISSAO_INICIO || ' à ' || :DEMISSAO_FIM AS PERIODO_GERACAO
  FROM CONTRATOS A
  JOIN SALARIOS B ON A.COD_CONTRATO = B.COD_CONTRATO
  LEFT JOIN ANTECIPACAO C ON A.COD_CONTRATO = C.COD_CONTRATO
