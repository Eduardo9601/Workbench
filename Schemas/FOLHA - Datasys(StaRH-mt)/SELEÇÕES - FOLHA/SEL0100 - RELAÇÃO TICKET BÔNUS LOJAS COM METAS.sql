       COD_UNIDADE,
       CPF,
       DES_PESSOA,
       CASE
           WHEN DES_EMAIL IS NOT NULL THEN
             DES_EMAIL
           ELSE
            DES_EMAIL_ALTER
        END AS "E-MAIL",
        COD_DDD || FONE_CEL AS "TELEFONE_CELULAR",
