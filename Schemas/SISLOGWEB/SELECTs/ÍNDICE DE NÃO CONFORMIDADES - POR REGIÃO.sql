       MAX(DECODE(TO_CHAR(BASE.DTA_MOVIMENTO,'DD/MM/YYYY'),'01/01/2026', BASE.COD_REGIAO_AJUSTADA, NULL)) AS COD_REGIAO_MES3,

       BASE.INVENT_MAX,

