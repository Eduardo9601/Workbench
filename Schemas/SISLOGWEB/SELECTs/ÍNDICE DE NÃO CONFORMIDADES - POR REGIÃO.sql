       SUM(DECODE(TO_CHAR(BASE.DTA_MOVIMENTO,'DD/MM/YYYY'),'01/12/2025', BASE.CUP_CANC,0)) AS CUP_CANC_MES2,
       SUM(DECODE(TO_CHAR(BASE.DTA_MOVIMENTO,'DD/MM/YYYY'),'01/01/2026', BASE.CUP_CANC,0)) AS CUP_CANC_MES3,

