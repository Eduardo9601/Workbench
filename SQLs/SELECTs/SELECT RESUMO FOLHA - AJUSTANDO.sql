  )
SELECT
  -- Lista de colunas sem a coluna EDICAO_ORG_REP
  EDICAO_EMP,
  NOME_ORGANOGRAMA,
  REFERENCIA,
  COD_VD_V,
  NOME_VD_V,
  TOTAL_MES_V,
  COD_VD_D,
  NOME_VD_D,
  TOTAL_MES_D,
  COD_VD_O,
  NOME_VD_O,
  TOTAL_MES_O,
  TOTAL_V,
  TOTAL_D,
  TOTAL_LIQUIDO
FROM
  Final_Result
ORDER BY
  EDICAO_ORG_REP ASC, -- Usando a coluna de repetição para ordenação
  ORDER_COL ASC,
  RN ASC

