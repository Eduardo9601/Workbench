--UPDATE PARA ATUALIZAR OS EMAIS DAS LOJAS

--EX: REDE GZT

UPDATE PESSOA_JURIDICA
SET EMAIL = CONCAT(SUBSTR(EMAIL, 1, INSTR(EMAIL, '@')), 'gztstore.com.br')
WHERE COD_PESSOA IN (
    SELECT J.COD_PESSOA
    FROM SISLOGWEB.V_UNIDADES_ATIVAS@NLGRZ UNI, JURIDICA J
    WHERE UNI.NUM_CGC = J.CGC
    AND J.FANTASIA = 'GZT'
)
AND SUBSTR(EMAIL, INSTR(EMAIL, '@') + 1) <> 'gztstore.com.br';
