CREATE VIEW VD_CONTAS_CONTABEIS AS
SELECT 
    RHFP1000.COD_VD,
    RHFP1000.NOME_VD,
    RHFP0404.COD_CONTA_CONTABIL,
    RHFP0403.NOME_CONTA_CONTABIL,
    RHFP0404.COD_CONTRAPARTIDA
FROM 
    RHFP1000 
    JOIN RHFP0404 ON RHFP1000.COD_VD = RHFP0404.COD_LANCAMENTO
    JOIN RHFP0403 ON RHFP0404.COD_CONTA_CONTABIL = RHFP0403.COD_CONTA_CONTABIL


SELECT * FROM VD_CONTAS_CONTABEIS