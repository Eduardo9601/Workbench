
SELECT A.COD_PESSOA, C.NOME_ORGANOGRAMA, A.COD_CAMPO, B.EDICAO_NIVEL3,  B.COD_ORGANOGRAMA, A.VALOR
FROM PE0002 A, RHFP0401 B, RHFP0400 C, JURIDICA JU
WHERE B.COD_ORGANOGRAMA = C.COD_ORGANOGRAMA
AND C.COD_PESSOA  = JU.COD_PESSOA 
AND JU.COD_PESSOA  = A.COD_PESSOA 
AND COD_CAMPO = 1 
--AND VALOR = 8701
AND B.COD_ORGANOGRAMA IN (
'1782',
'1784',
'1785'
)
ORDER BY A.VALOR,B.EDICAO_NIVEL3 ASC




