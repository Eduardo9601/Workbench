CREATE OR REPLACE FUNCTION get_hours_from_min_f
  (
    p_minutes NUMBER
  ) RETURN VARCHAR2 IS

ln_hours    NUMBER;
ln_minutes  NUMBER;
lv_return   VARCHAR2(10);

BEGIN
   ln_hours   := TRUNC(p_minutes/60);
   ln_minutes := MOD(p_minutes,60);

   lv_return  := ln_hours || ':' || LPAD(ln_minutes,2,'0');
   RETURN lv_return;
END;



