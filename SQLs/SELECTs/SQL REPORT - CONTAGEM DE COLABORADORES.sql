WITH CTE_DADOS AS (
    SELECT DISTINCT
           AFAST.STATUS,
           CTR.COD_CONTRATO,
           ORGCT.COD_ORGANOGRAMA,
           ORG.EDICAO_ORG AS COD_UNIDADE,
           ORG2.NOME_ORGANOGRAMA_B,
           ORG2.COD_REDE,
           ORG2.DES_REDE,
           ORG2.COD_TIPO,
           ORG2.DES_TIPO,
           FUN2.NOME_CLH AS DES_FUNCAO,
           FUN.DATA_INICIO,
           FUN.DATA_FIM,
           AFAST.COD_AFAST,
           AFAST.DATA_INI_AFAST,
           AFAST.DATA_FIM_AFAST
    FROM RHFP0300 CTR
    INNER JOIN RHFP0310 ORGCT
        ON CTR.COD_CONTRATO = ORGCT.COD_CONTRATO
    INNER JOIN RHFP0340 FUN
        ON CTR.COD_CONTRATO = FUN.COD_CONTRATO
        AND :DATA_REFERENCIA BETWEEN FUN.DATA_INICIO AND FUN.DATA_FIM
    INNER JOIN RHFP0500 FUN2
        ON FUN.COD_CLH = FUN2.COD_CLH
    LEFT JOIN VH_AFAST_CONTRATO_AVT AFAST
        ON CTR.COD_CONTRATO = AFAST.COD_CONTRATO
        AND :DATA_REFERENCIA BETWEEN AFAST.DATA_INI_AFAST AND AFAST.DATA_FIM_AFAST
    LEFT JOIN RHFP0401 ORG
        ON ORGCT.COD_ORGANOGRAMA = ORG.COD_ORGANOGRAMA
        AND :DATA_REFERENCIA BETWEEN ORGCT.DATA_INICIO AND ORGCT.DATA_FIM
    LEFT JOIN V_EST_ORG_ALTER_AVT ORG2
        ON ORG.COD_ORGANOGRAMA = ORG2.COD_ORGANOGRAMA
        /*AND ORG2.STATUS_ORG = 0*/
        AND (:DATA_REFERENCIA BETWEEN ORG2.DATA_INICIO AND ORG2.DATA_FIM)
    WHERE (
        (:DATA_REFERENCIA BETWEEN CTR.DATA_INICIO AND CTR.DATA_FIM)
        OR (CTR.DATA_INICIO <= :DATA_REFERENCIA AND CTR.DATA_FIM IS NULL)
    )
    AND ORG.COD_NIVEL2 = 8
    --AND ORG2.COD_REDE NOT IN ('001', '013')
),
CTE_AGREGADA AS (
    SELECT
           COD_REDE,
           DES_REDE,
           COUNT(DISTINCT COD_CONTRATO) AS QTD_CONTRATOS_GERAL,

           COUNT(DISTINCT CASE
                   WHEN DES_FUNCAO NOT LIKE '%SERVENTE%'
                        AND DES_FUNCAO NOT LIKE '%ESTAGIARIO%'
                        AND DES_FUNCAO NOT LIKE '%APRENDIZ%'
                        AND (COD_AFAST IN (6,7) OR COD_AFAST IS NULL)
                   THEN COD_CONTRATO
                 END) AS QTD_COLABORADORES,

           COUNT(DISTINCT CASE
                   WHEN DES_FUNCAO LIKE '%ESTAGIARIO%'
                        AND (COD_AFAST IN (6,7) OR COD_AFAST IS NULL)
                   THEN COD_CONTRATO
                 END) AS QTD_ESTAGIARIOS,

           COUNT(DISTINCT CASE
                   WHEN DES_FUNCAO LIKE '%APRENDIZ%'
                        AND (COD_AFAST IN (6,7) OR COD_AFAST IS NULL)
                   THEN COD_CONTRATO
                 END) AS QTD_APRENDIZ,

           COUNT(DISTINCT CASE
                   WHEN DES_FUNCAO LIKE '%SERVENTE%'
                        AND (COD_AFAST IN (6,7) OR COD_AFAST IS NULL)
                   THEN COD_CONTRATO
                 END) AS QTD_SERVENTES,

           COUNT(DISTINCT CASE
                   WHEN COD_AFAST NOT IN (6,7)
                   THEN COD_CONTRATO
                 END) AS QTD_AFASTADOS
      FROM CTE_DADOS
     GROUP BY COD_REDE, DES_REDE
)
SELECT A.*,
       SUM(A.QTD_CONTRATOS_GERAL) OVER() AS QTD_TOTAL_CONTRATOS,

       SUM(A.QTD_COLABORADORES) OVER() AS QTD_TOTAL_COLAB,

       SUM(A.QTD_ESTAGIARIOS) OVER() AS QTD_TOTAL_ESTAGIARIOS,
       SUM(A.QTD_APRENDIZ) OVER() AS QTD_TOTAL_APRENDIZES,
       SUM(A.QTD_SERVENTES) OVER() AS QTD_TOTAL_SERVENTES,
       SUM(A.QTD_AFASTADOS) OVER() AS QTD_TOTAL_AFASTADOS

  FROM CTE_AGREGADA A
 ORDER BY A.COD_REDE


