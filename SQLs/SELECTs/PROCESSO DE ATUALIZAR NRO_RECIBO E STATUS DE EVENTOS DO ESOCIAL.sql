UPDATE RHES0110 A
SET 
    A.COD_SITUACAO = 3,
    A.COD_RESPOSTA = 201,
    A.DESCRICAO_RESPOSTA = 'SUCESSO',
    A.NRO_RECIBO = (
        SELECT B.NRO_RECIBO
        FROM RHES0110_TB B
        WHERE B.COD_EVENTO = A.COD_EVENTO
    ),
    A.IND_ASSINADO = 'S'
WHERE A.COD_ROTINA = 1449
  AND EXISTS (
      SELECT 1
      FROM RHES0110_TB B
      WHERE B.COD_EVENTO = A.COD_EVENTO
  );
