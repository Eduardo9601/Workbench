SELECT a.*, rowid--COD_EVENTO  
FROM RHES0110 a
WHERE a.COD_ROTINA = 1461
AND a.COD_EVENTO IN (18098, 18099, 18100)
AND a.NRO_RECIBO IS NULL
ORDER BY a.COD_EVENTO


SELECT COD_EVENTO  
FROM RHES0110
WHERE COD_ROTINA = 1449
AND TAB_EVENTO = 'S-1210'
AND NRO_RECIBO IS NULL
ORDER BY COD_EVENTO


SELECT * FROM RHES1210
