
--SCRIPT PARA INSERIR UM NOVO VALOR NA TABELA PE0002

--INSERT DO INDICADOR PARA SINALIZAR SE A LOJA ESTÁ ATIVA


INSERT INTO PE0002 (COD_PESSOA, COD_CAMPO, VALOR)
SELECT DISTINCT COD_PESSOA, 0 AS COD_CAMPO, 0 AS VALOR --INDICA EM ATIVIDADE
FROM PE0002
WHERE COD_CAMPO = 1
  AND VALOR IN (8701, 8702, 8703, 8704, 8705, 8706, 8707, 8708, 8709, 8710, 8711, 8712, 8713, 8714, 8715, 8716)
  AND COD_PESSOA NOT IN (
    SELECT DISTINCT COD_PESSOA
    FROM PE0002
    WHERE COD_CAMPO = 0
  );




