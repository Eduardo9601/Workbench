SELECT A.COD_VD,
       C.NOME_VD,
       TO_CHAR(SUM(A.VALOR_VD), 'FM999G999G990D00') AS VALOR
FROM RHFP1006 A
INNER JOIN RHFP1003 B ON A.COD_MESTRE_EVENTO = B.COD_MESTRE_EVENTO
INNER JOIN RHFP1000 C ON A.COD_VD = C.COD_VD
WHERE B.DATA_REFERENCIA BETWEEN '01/10/2024' AND '31/10/2024'
AND B.COD_EVENTO IN (17,19)
AND A.COD_VD IN (1001, 1002, 1003, 896)
GROUP BY A.COD_VD,
         C.NOME_VD
ORDER BY A.COD_VD