SELECT DISTINCT CT.COD_CONTRATO, FUN.NOME_FUNCIONARIO, CT.DATA_FIM, EDI.EDICAO_ORG
FROM RHFP0300 CT, RHFP0310 ORG, RHFP0401 EDI, FUNCIONARIOS FUN
WHERE FUN.COD_CONTRATO = CT.COD_CONTRATO
AND CT.COD_CONTRATO = ORG.COD_CONTRATO
AND ORG.COD_ORGANOGRAMA = EDI.COD_ORGANOGRAMA
AND CT.DATA_FIM >= '01/01/2023'
AND EDI.EDICAO_ORG IN (780, 781, 782, 783, 784, 785, 786)
ORDER BY CT.DATA_FIM, CT.COD_CONTRATO ASC