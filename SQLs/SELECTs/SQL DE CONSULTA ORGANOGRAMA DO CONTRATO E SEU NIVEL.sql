CONSULTA ORGANOGRAMA DO CONTRATO E SEU NIVEL

SELECT A.COD_CONTRATO,
       A.COD_ORGANOGRAMA,
       B.COD_ORGANOGRAMA,
       B.EDICAO_ORG,
       B.NOME_ORGANOGRAMA,
       B.COD_NIVEL_ORG
FROM RHFP0310 A
INNER JOIN V_ORGANOGRAMAS_NIVEIS B ON A.COD_ORGANOGRAMA = B.COD_ORGANOGRAMA
INNER JOIN RHFP0300 C ON A.COD_CONTRATO = C.COD_CONTRATO
WHERE A.DATA_FIM = '31/12/2999'
AND C.DATA_FIM IS NULL
AND B.COD_NIVEL_ORG <> 5 --OR B.COD_NIVEL_ORG <> 5 OR B.COD_NIVEL_ORG <> 6