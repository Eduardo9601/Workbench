---==SQL PARA ENCONTRAR TABELAS

SELECT DISTINCT(TABLE_NAME) 
FROM ALL_TAB_COLUMNS 
WHERE UPPER (TABLE_NAME) LIKE '%RH%'
AND COLUMN_NAME LIKE '%%'
AND OWNER = 'GRAZZ';


--==SQL PARA ENCONTRAR VIEWS

SELECT DISTINCT(TABLE_NAME) 
FROM ALL_TAB_COLUMNS 
WHERE UPPER(TABLE_NAME) LIKE '%DITO%'
AND COLUMN_NAME LIKE '%%'
AND TABLE_NAME IN (SELECT VIEW_NAME FROM ALL_VIEWS);


