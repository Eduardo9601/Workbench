CREATE OR REPLACE VIEW V_TIPOS_ORG_AVT AS
SELECT
      COD_ORGANOGRAMA,
      EDICAO_ORG,
    CASE
        WHEN EDICAO_NIVEL3 = '013' AND EDICAO_NIVEL2 = '008' THEN 3
        WHEN EDICAO_NIVEL3 = '001' AND EDICAO_NIVEL2 = '008' THEN 2
        WHEN EDICAO_NIVEL3 = '001' AND EDICAO_NIVEL2 = '004' OR
             EDICAO_NIVEL3 = '001' AND EDICAO_NIVEL2 = '006' OR
             EDICAO_NIVEL3 = '002' AND EDICAO_NIVEL2 = '128' OR
             EDICAO_NIVEL3 = '002' AND EDICAO_NIVEL2 = '276' OR
             EDICAO_NIVEL3 = '001' AND EDICAO_NIVEL2 = '282' THEN 4
        ELSE 1
    END AS COD_TIPO,
    CASE
        WHEN EDICAO_NIVEL3 = '013' AND EDICAO_NIVEL2 = '008' THEN 'DEPÓSITO'
        WHEN EDICAO_NIVEL3 = '001' AND EDICAO_NIVEL2 = '008' THEN 'SETOR'
        WHEN EDICAO_NIVEL3 = '001' AND EDICAO_NIVEL2 = '004' OR
             EDICAO_NIVEL3 = '001' AND EDICAO_NIVEL2 = '006' OR
             EDICAO_NIVEL3 = '002' AND EDICAO_NIVEL2 = '128' OR
             EDICAO_NIVEL3 = '002' AND EDICAO_NIVEL2 = '276' OR
             EDICAO_NIVEL3 = '001' AND EDICAO_NIVEL2 = '282' THEN 'COLIGADA'
        ELSE 'LOJA'
    END AS DES_TIPO
FROM RHFP0401

