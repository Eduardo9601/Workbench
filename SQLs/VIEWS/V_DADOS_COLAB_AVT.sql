       ORG.DES_TIPO,

       /*DADOS DO QUADRO DE LOTAÇÃO DO CONTRATO*/
       LOT.COD_LOTACAO,
       LOT.DES_LOTACAO,
       LOT.UNI_LOTACAO,
       LOT.DTA_INICIO_LOT,
       LOT.DTA_FIM_LOT,

