CREATE OR REPLACE VIEW V_DADOS_COLAB_AVT AS
WITH
CONTRATOS AS (
SELECT CASE
         WHEN A.DATA_FIM IS NOT NULL AND A.DATA_FIM < SYSDATE THEN
          1
         ELSE
          0
       END AS STATUS,
       CASE
         WHEN A.DATA_FIM IS NOT NULL AND A.DATA_FIM < SYSDATE THEN
          'INATIVO'
         WHEN A.DATA_INICIO <= SYSDATE /*OR CT.DATA_FIM = SYSDATE AND TO_CHAR(SYSDATE, 'HH24:MI:SS') = '23:59:59'*/
          THEN
          'ATIVO'
         WHEN A.DATA_INICIO > SYSDATE
         /*AND CT.DATA_AVANCO > SYSDATE*/
          THEN
          'ADMISSAO FUTURA'
       END AS DESC_STATUS,

       B.COD_PESSOA,
       A.COD_CONTRATO,
       INITCAP(B.NOME_PESSOA) AS DES_PESSOA,
       INITCAP(REGEXP_SUBSTR(B.NOME_PESSOA, '^\S+')) AS PRIMEIRO_NOME,
       INITCAP(REGEXP_SUBSTR(B.NOME_PESSOA, ' (.+) ')) AS NOMES_INTERMEDIARIOS,
       INITCAP(REGEXP_SUBSTR(B.NOME_PESSOA, '\S+$')) AS ULTIMO_NOME,
       A.DATA_AVANCO,
       A.DATA_INICIO AS DATA_ADMISSAO,
       A.DATA_FIM AS DATA_DEMISSAO,
       A.COD_CAUSA_DEMISSAO AS COD_DEMISSAO,
       C.NOME_CAUSA_DEMISSAO AS DES_DEMISSAO,
       A.COD_VINCULO_EMPREG,
       A.COD_CATEGORIA_TRAB,
       G.COD_ESOCIAL,
       A.NRO_CONTA_PGTO || '/' || A.COD_AGE_PGTO AS CONTA_BANCARIA,
       A.COD_BCO_PGTO AS COD_BANCO,
       F.NOME_PESSOA AS DES_BANCO
  FROM RHFP0300 A
 INNER JOIN PESSOA_FISICA B ON A.COD_FUNC = B.COD_PESSOA
  LEFT JOIN RHFP0102 C ON A.COD_CAUSA_DEMISSAO = C.COD_CAUSA_DEMISSAO
  LEFT JOIN RHFP0128 D ON A.COD_CATEGORIA_TRAB = D.COD_CATEGORIA_TRAB
  LEFT JOIN BANCO E ON A.COD_BCO_PGTO = E.COD_BANCO
  LEFT JOIN PESSOA F ON E.COD_PESSOA = F.COD_PESSOA
  LEFT JOIN RHFP0128 G ON A.COD_CATEGORIA_TRAB = G.COD_CATEGORIA_TRAB
 WHERE ((A.DATA_FIM IS NULL OR
       SYSDATE BETWEEN A.DATA_INICIO AND
       NVL(A.DATA_FIM, TO_DATE('31/12/2999', 'DD/MM/YYYY'))) OR
       (
       -- Mantém contratos ativos se a data de demissão for hoje, mas antes das 23:59:59
        A.DATA_FIM = TRUNC(SYSDATE) AND A.DATA_FIM < TRUNC(SYSDATE) + 1

       ) OR (
       -- Mantém contratos ativos se a data de demissão for no futuro
        A.DATA_FIM > TRUNC(SYSDATE)) OR (
       -- Mantém contratos ativos por 1850 dias após a demissão (caso necessário)
        A.DATA_FIM >= SYSDATE - 1250))


),

DADOS_PESSOAIS AS (
SELECT A.COD_FUNC,
       A.DATA_NASCIMENTO,
       TRUNC(MONTHS_BETWEEN(SYSDATE, A.DATA_NASCIMENTO) / 12) AS IDADE,
       A.SEXO,
       A.COD_ESTADO_CIVIL AS COD_EST_CIVIL,
       D.NOME_ESTADO_CIVIL AS DES_EST_CIVIL,
       A.COD_PESSOA_PAI,
       H1.NOME_PESSOA AS DES_PAI,
       A.COD_PESSOA_MAE,
       H2.NOME_PESSOA AS DES_MAE,
       A.IND_DEFICIENCIA,
       A.COD_DEFICIENCIA,
       F.NOME_DEFICIENCIA AS DES_DEFICIENCIA,
       I.EMAIL AS DES_EMAIL,
       I.DDD_FONE_CEL AS COD_DDD,
       I.FONE_CEL,
       I.CPF,
       A.NRO_IDENTIDADE,
       C.NOME_PESSOA || '/' || A.COD_UF_IDENTIDADE AS EMISSOR,
       A.NRO_PIS_PASEP,
       A.NRO_CTPS || '/' || A.NRO_SERIE_CTPS AS CTPS,
       A.DATA_EXP_CTPS,
       A.COD_GRAU_INSTRUCAO AS COD_INSTRUCAO,
       G.NOME_RESUMIDO AS DES_INSTRUCAO,
       A.COD_NACIONALIDADE,
       E.NOME_NACIONALIDADE || '(a)' AS NACIONALIDADE,
       A.COD_NACIONALIDADE || ' - ' || E.NOME_NACIONALIDADE || '(a)' AS DES_NACIONALIDADE,
       A.COD_RACA_COR,
       B.NOME_RACA_COR AS DES_RACA_COR,
       I.CEP AS COD_CEP,
       I.TIPO_LOGRA,
       I.COD_LOGRA,
       I.NOME_LOGRA AS DES_LOGRA,
       I.NUMERO,
       I.COD_BAIRRO,
       I.NOME_BAIRRO AS DES_BAIRRO,
       I.COD_MUNIC AS COD_CIDADE,
       I.NOME_MUNIC AS DES_CIDADE,
       I.COD_UF
  FROM RHFP0200 A
  LEFT JOIN RHFP0130 B ON A.COD_RACA_COR = B.COD_RACA_COR
  LEFT JOIN PESSOA_JURIDICA C ON A.COD_ORG_IDENTIDADE = C.COD_PESSOA
  LEFT JOIN RHFP0104 D ON A.COD_ESTADO_CIVIL = D.COD_ESTADO_CIVIL
  LEFT JOIN RHFP0107 E ON A.COD_NACIONALIDADE = E.COD_NACIONALIDADE
  LEFT JOIN RHFP0132 F ON A.COD_DEFICIENCIA = F.COD_DEFICIENCIA
  LEFT JOIN RHFP0105 G ON A.COD_GRAU_INSTRUCAO = G.COD_GRAU_INSTRUCAO
  LEFT JOIN PESSOA H1 ON A.COD_PESSOA_PAI = H1.COD_PESSOA
  LEFT JOIN PESSOA H2 ON A.COD_PESSOA_MAE = H2.COD_PESSOA
 INNER JOIN PESSOA_FISICA I ON A.COD_FUNC = I.COD_PESSOA
--WHERE A.COD_FUNC = 91735

),


FUNCAO AS (
SELECT A.COD_CONTRATO,
       A.COD_CLH AS COD_FUNCAO,
       B.NOME_CLH,
       A.COD_CLH || ' - ' || B.NOME_CLH AS DES_FUNCAO,
       A.DATA_INICIO AS DATA_INICIO_CLH,
       A.DATA_FIM AS DATA_FIM_CLH
  FROM RHFP0340 A
  JOIN RHFP0500 B ON A.COD_CLH = B.COD_CLH
 WHERE (SYSDATE BETWEEN A.DATA_INICIO AND
       NVL(A.DATA_FIM, TO_DATE('31/12/2999', 'DD/MM/YYYY')) OR
       A.DATA_INICIO >= SYSDATE)
       --AND A.COD_CONTRATO = 397436

),


TURNO AS (
SELECT A.COD_CONTRATO,
       CASE
         WHEN A.COD_TURNO = 85 THEN
          'N'
         ELSE
          'S'
       END AS IND_BATE_PONTO,
       A.COD_TURNO,
       B.NOME_TURNO AS DES_TURNO,
       /*HR.QTD_HORBAS_MES AS HR_BASE_MES,
         HR.QTD_HORBAS_SEM AS HR_BASE_SEM,
         HR.QTD_HORBAS_DIA AS HR_BASE_DIA,*/
       LTRIM(RTRIM(TRIM(TRAILING '.' FROM TRIM(TRAILING '0' FROM TO_CHAR(C.QTD_HORBAS_MES, '9999999990.99'))))) AS HR_BASE_MES,
       LTRIM(RTRIM(TRIM(TRAILING '.' FROM TRIM(TRAILING '0' FROM TO_CHAR(C.QTD_HORBAS_SEM, '9999999990.99'))))) AS HR_BASE_SEM,
       LTRIM(RTRIM(TRIM(TRAILING '.' FROM TRIM(TRAILING '0' FROM TO_CHAR(C.QTD_HORBAS_DIA, '9999999990.99'))))) AS HR_BASE_DIA,
       C.DATA_INICIO AS DATA_INICIO_HR,
       C.DATA_FIM AS DATA_FIM_HR
  FROM RHAF1119 A
  LEFT JOIN RHAF1145 B ON A.COD_TURNO = B.COD_TURNO
  LEFT JOIN V_HORAS_COLAB_AVT C ON A.COD_CONTRATO = C.COD_CONTRATO
 WHERE NVL(A.DATA_FIM, TO_DATE('31/12/2999','DD/MM/YYYY')) >= TRUNC(SYSDATE)
 --AND A.COD_CONTRATO = 397436

),

HORARIOS AS (
SELECT A.COD_CONTRATO,
       A.DATA_INICIO AS DATA_INICIO_HOR,
       A.DATA_FIM AS DATA_FIM_HOR,
       A.COD_TURNO,
       D.NOME_TURNO AS DES_TURNO,
       A.COD_MOTIVO,
       B.NOME_MOTIVO,
       A.TIPO_TURNO,
       DECODE(D.HORARIO_1_ENT,
              NULL,
              NULL,
              LPAD(RHYF0118(RHYF0117(D.HORARIO_1_ENT), 'S'), 5, '0')) AS HOR_1_ENT,
       DECODE(D.HORARIO_1_SAI,
              NULL,
              NULL,
              LPAD(RHYF0118(RHYF0117(D.HORARIO_1_SAI), 'S'), 5, '0')) AS HOR_1_SAI,
       DECODE(D.HORARIO_2_ENT,
              NULL,
              NULL,
              LPAD(RHYF0118(RHYF0117(D.HORARIO_2_ENT), 'S'), 5, '0')) AS HOR_2_ENT,
       DECODE(D.HORARIO_2_SAI,
              NULL,
              NULL,
              LPAD(RHYF0118(RHYF0117(D.HORARIO_2_SAI), 'S'), 5, '0')) AS HOR_2_SAI,
       DECODE(D.HORARIO_3_ENT,
              NULL,
              NULL,
              LPAD(RHYF0118(RHYF0117(D.HORARIO_3_ENT), 'S'), 5, '0')) AS HOR_3_ENT,
       DECODE(D.HORARIO_3_SAI,
              NULL,
              NULL,
              LPAD(RHYF0118(RHYF0117(D.HORARIO_3_SAI), 'S'), 5, '0')) AS HOR_3_SAI,
       DECODE(D.HORARIO_4_ENT,
              NULL,
              NULL,
              LPAD(RHYF0118(RHYF0117(D.HORARIO_4_ENT), 'S'), 5, '0')) AS HOR_4_ENT,
       DECODE(D.HORARIO_4_SAI,
              NULL,
              NULL,
              LPAD(RHYF0118(RHYF0117(D.HORARIO_4_SAI), 'S'), 5, '0')) AS HOR_4_SAI
  FROM RHAF1119 A, RHFP0323 B, RHFP0115 C, RHAF1145 D, RHAF1103 F
 WHERE A.COD_MOTIVO = B.COD_MOTIVO(+)
   AND B.COD_TIPO_MOTIVO = C.COD_TIPO_MOTIVO(+)
   AND A.COD_TURNO = D.COD_TURNO(+)
   AND D.COD_CLASSE_TURNO = F.COD_CLASSE_TURNO(+)
   --AND TRUNC(SYSDATE) BETWEEN A.DATA_INICIO AND A.DATA_FIM
   AND NVL(A.DATA_FIM, TO_DATE('31/12/2999','DD/MM/YYYY')) >= TRUNC(SYSDATE)
   --AND A.COD_CONTRATO = 397436

),

SINDICATO AS (
SELECT COD_CONTRATO,
       COD_SINDICATO,
       DES_SINDICATO,
       IND_CONT_SINDICAL,
       DATA_INICIO AS DATA_INI_SIND,
       DATA_FIM AS DATA_FIM_SIND,
       ROW_NUMBER() OVER(PARTITION BY COD_CONTRATO ORDER BY DATA_INICIO DESC) AS RN
  FROM V_SINDICATOS_CONTRATOS_AVT
  --WHERE COD_CONTRATO = 390511

),

ORGANOGRAMA AS (
SELECT ORG.*,
       ROW_NUMBER() OVER(PARTITION BY ORG.COD_CONTRATO ORDER BY ORG.DATA_INI_ORG DESC) AS RN
  FROM VH_EST_ORG_CONTRATO_AVT ORG
--LEFT JOIN V_ORGANOGRAMAS_AVT VO ON ORG.COD_ORGANOGRAMA = VO.COD_ORGANOGRAMA
/*WHERE SYSDATE BETWEEN ORG.DATA_INICIO AND
     NVL(ORG.DATA_FIM, TO_DATE('31/12/2999', 'DD/MM/YYYY'))*/

),

RESPONSAVEL AS (
SELECT COD_CONTRATO,
       IND_RESP_UNI
FROM V_SUPERIOR_IMEDIATO_AVT

),

LOTACAO AS (
SELECT A.COD_CONTRATO,
       CASE
         WHEN A.COD_LOTACAO IS NOT NULL THEN
          A.COD_LOTACAO
         ELSE
          NULL
       END AS COD_LOTACAO,
       CASE
         WHEN A.COD_LOTACAO IS NOT NULL THEN
          B.NOME_LOTACAO
         ELSE
          NULL
       END AS DES_LOTACAO,
       CASE
         WHEN A.COD_LOTACAO IS NOT NULL THEN
          B.EDICAO_QL
         ELSE
          NULL
       END AS UNI_LOTACAO,
       CASE
         WHEN A.COD_LOTACAO IS NOT NULL THEN
          A.DATA_INICIO
         ELSE
          NULL
       END AS DTA_INICIO_LOT,
       CASE
         WHEN A.COD_LOTACAO IS NOT NULL THEN
          A.DATA_FIM
         ELSE
          NULL
       END AS DTA_FIM_LOT,

       ROW_NUMBER() OVER(PARTITION BY A.COD_CONTRATO ORDER BY A.COD_LOTACAO DESC) AS RN
  FROM RHFP0309 A
  LEFT JOIN RHFP0509 B ON A.COD_LOTACAO = B.COD_LOTACAO
  --WHERE A.COD_CONTRATO = 372102
/*WHERE SYSDATE BETWEEN LOT_CONT.DATA_INICIO AND
       NVL(LOT_CONT.DATA_FIM, TO_DATE('31/12/2999', 'DD/MM/YYYY'))*/
  ORDER BY A.COD_LOTACAO

),


AFASTAMENTOS AS (
SELECT AFAST.COD_CONTRATO,
       /*AFAST.COD_CAUSA_AFAST,
                          AFAST.NOME_CAUSA_AFAST,
                          AFAST.DATA_INICIO,
                          AFAST.DATA_FIM,*/

       AFAST.COD_CAUSA_AFAST AS COD_AFAST,
       CASE
         WHEN AFAST.COD_CAUSA_AFAST IS NOT NULL THEN
          AFAST.COD_CAUSA_AFAST || ' - ' || AFAST.NOME_CAUSA_AFAST
         ELSE
          NULL
       END AS DES_AFAST,
       AFAST.DATA_INICIO AS DATA_INI_AFAST,
       AFAST.DATA_FIM AS DATA_FIM_AFAST,
       CASE
         WHEN AFAST.COD_CAUSA_AFAST IS NULL OR
              TRIM(TO_CHAR(AFAST.COD_CAUSA_AFAST)) IS NULL THEN
          0
         ELSE
          AFAST.COD_CAUSA_AFAST
       END AS COD_STATUS_AFAST,
       CASE
         WHEN CT.DATA_FIM IS NOT NULL AND CT.DATA_FIM < SYSDATE THEN
          'INATIVO'
         WHEN AFAST.COD_CAUSA_AFAST <> 0 AND AFAST.COD_CAUSA_AFAST <> 4 AND
              AFAST.COD_CAUSA_AFAST <> 21 AND AFAST.COD_CAUSA_AFAST <> 60 AND
              AFAST.DATA_FIM = TO_DATE('31/12/2999', 'DD/MM/YYYY') THEN
          'AFASTADO'
         WHEN AFAST.COD_CAUSA_AFAST IN (21, 60) THEN
          'ATIVO, SEM RETORNO'
         WHEN AFAST.COD_CAUSA_AFAST = 7 THEN
          'EM FÉRIAS'
         WHEN AFAST.COD_CAUSA_AFAST IN (3, 6) THEN
          'AFASTADO TEMP'
         WHEN AFAST.COD_CAUSA_AFAST = 4 AND
              AFAST.DATA_FIM = TO_DATE('31/12/2999', 'DD/MM/YYYY') THEN
          'PODERÁ RETORNAR'
         ELSE
          'EM ATIVIDADE'
       END AS STATUS_AFAST,

       ROW_NUMBER() OVER(PARTITION BY AFAST.COD_CONTRATO ORDER BY AFAST.DATA_INICIO DESC) AS RN
  FROM V_AFAST_COLAB_AVT AFAST
  JOIN RHFP0300 CT ON AFAST.COD_CONTRATO = CT.COD_CONTRATO
--WHERE AFAST.COD_CONTRATO =
),

FERIAS AS (
SELECT COD_CONTRATO,
       DATA_PREVISTA_FERIAS,
       DATA_FERIAS,
       DATA_RETORNO
  FROM VH_PROGRAMACAO_FERIAS_AVT
 WHERE IND_FERIAS = 0

),


/*DADOS ANTERIORES AO ATUAL DO CONTRATO*/
