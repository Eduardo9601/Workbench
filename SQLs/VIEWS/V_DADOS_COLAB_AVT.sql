ORGANOGRAMA_ANT AS (
SELECT CO.COD_CONTRATO,
       CO.EDICAO_ORG_ANT,
       CO.DES_ORGANOGRAMA_ANT,
       CO.DATA_INICIO_ORG_ANT,
       CO.DATA_FIM_ORG_ANT,
       ROW_NUMBER() OVER(PARTITION BY CO.COD_CONTRATO ORDER BY CO.DATA_INICIO_ORG_ANT DESC) AS RN
