       CT.ULTIMO_NOME,
       CT.DATA_AVANCO,
       CT.DATA_ADMISSAO,
       CT.DATA_DEMISSAO,
       CT.COD_DEMISSAO,
       CT.DES_DEMISSAO,
       CT.CONTA_BANCARIA,
       CT.COD_BANCO,
       CT.DES_BANCO,
       CT.STATUS,
       CT.DESC_STATUS,
       FU.COD_FUNCAO,
       FU.NOME_CLH,
