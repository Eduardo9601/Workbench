       SI.IND_CONT_SINDICAL,
       SI.DATA_INI_SIND,
       SI.DATA_FIM_SIND,

       /*DADOS DO ORGANOGRAMA DO CONTRATO*/
       ORG.COD_ORGANOGRAMA,
