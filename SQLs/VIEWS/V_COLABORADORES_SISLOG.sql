CREATE OR REPLACE VIEW V_COLABORADORES AS
SELECT VC.COD_FUNCIONARIO,
       GRZ_UTIL.remove_ponto_numeros(VC.COD_CENTRO_CUSTO) COD_CENTRO_CUSTO,
       VC.DES_NOME,
       VC.DTA_ADMISSAO,
       VC.DTA_DEMISSAO,
       VC.DATA_NASCIMENTO,
       GRZ_UTIL.only_numbers(VC.COD_ORGANOGRAMA) COD_ORGANOGRAMA,
       VC.COD_EMPRESA,
       VC.DES_EMPRESA,
       VC.COD_FUNCAO,
       VC.COD_TURNO,
       VC.QTD_CH_MENSAL,
       VC.NUM_CART_TRAB,
       VC.NUM_SERIE_CART_TRAB,
       VC.COD_AFASTAMENTO,
       VC.DATA_AFASTAMENTO_INI,
       VC.DATA_AFASTAMENTO_FIM,
       VC.CPF,
       VC.SEXO,
       VC.CARGO,
       VC.DES_MUNIC,
       VC.EMAIL,
       VC.IND_GESTOR,
       GRZ_COLABORADORES_PCKG.GESTOR_IMEDIATO(GRZ_UTIL.only_numbers(VC.COD_ORGANOGRAMA), VC.COD_FUNCIONARIO) CPF_GESTOR
   FROM GRZ_FOLHA.V_COLABORADORES@NLGZT VC

