CREATE OR REPLACE VIEW NL.DATALAKE_DIM_REGIAO AS
SELECT
    a.cod_grupo AS COD_REGIAO,
    a.des_grupo AS NOME_REGIAO
FROM
    ge_grupos a
WHERE
    a.cod_grupo IN (8701,8702,8703,8704,8705,8706,8707,8708,8709,8710,8711,8712,8713,8714,8715,8716,8717,8718,8719, 8720, 8721, 8722) --ESSAS 2 ÚLTIMAS, PODE NÃO EXISTIR, MAS FICA COMO RESERVA
    AND a.cod_emp = 1

UNION ALL

SELECT
    7549 AS COD_REGIAO,
    'ECOMMERCE' AS NOME_REGIAO
FROM
    dual