         ROUND((COUNT(CASE WHEN A.SEXO = 'F' /*AND A.DES_FUNCAO NOT LIKE '%APRENDIZ%'*/ THEN 1 END) * 100.0 / COUNT(*)), 2) AS PERC_FEM,
