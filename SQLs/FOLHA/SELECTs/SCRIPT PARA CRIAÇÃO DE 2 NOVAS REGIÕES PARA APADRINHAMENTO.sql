SELECT
    OP.COD_OPERADOR,
    VDP.DES_PESSOA,
    PE.VALOR,
    PE.COD_PESSOA,
    ORG.COD_ORGANOGRAMA,
    EDIC.EDICAO_ORG,
    ORG.NOME_ORGANOGRAMA
FROM
    V_DADOS_PESSOA VDP,
    PE0002         PE,
    RHFP0300       CT,
    OPERADOR       OP,
    RHFP0400       ORG,
    RHFP0401       EDIC
WHERE
        PE.VALOR = VDP.COD_GRUPO_UNIDADE
    AND CT.COD_CONTRATO = VDP.COD_CONTRATO
    AND OP.COD_PESSOA = CT.COD_FUNC
    AND ORG.COD_PESSOA = PE.COD_PESSOA
    AND EDIC.COD_ORGANOGRAMA = ORG.COD_ORGANOGRAMA
    AND COD_FUNCAO = 325 -- REGIONAIS
    AND PE.COD_CAMPO = 1
    AND PE.VALOR = 8715
    --AND EDIC.EDICAO_ORG IN (242,605,467,7183,269)
    --AND VDP.COD_CONTRATO = 
ORDER BY EDIC.EDICAO_ORG;

