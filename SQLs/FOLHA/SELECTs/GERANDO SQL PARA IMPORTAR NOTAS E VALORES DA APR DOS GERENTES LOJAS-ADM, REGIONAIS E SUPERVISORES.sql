 INNER JOIN V_DADOS_PESSOA B ON A.COD_CONTRATO = B.COD_CONTRATO
 WHERE A.COD_VD = 219
   AND A.COD_EVENTO = 9
   AND A.DATA_MOV = '30/04/2023'
