       B.DES_PESSOA, 
       B.DES_FUNCAO,
       '/============/' AS ESPACO,
       A.COD_EVENTO, 
       A.COD_VD,
       A.DATA_MOV,
       A.TIPO_INF_VD,
       A.QTDE_VD,
       A.VALOR_VD
