accept PASSWORD_SYS prompt "SENHA DO USUARIO SYS: " HIDE
accept OWNER prompt "NOME DO NOVO USUARIO (OWNER)  : "
accept SENHA prompt "SENHA NOVO USUARIO (OWNER)    : " HIDE
accept DEF prompt "DEFAULT TABLESPACE   : "
accept TMP prompt "TEMPORARY TABLESPACE : "
accept HOST_STRING prompt "HOST_STRING  : "

CONNECT SYS/&&PASSWORD_SYS@&&HOST_STRING AS SYSDBA;

CREATE USER &&OWNER IDENTIFIED BY &&SENHA DEFAULT TABLESPACE &&DEF TEMPORARY TABLESPACE &&TMP;

GRANT CONNECT, RESOURCE, ALTER USER, UNLIMITED TABLESPACE, CREATE ANY SYNONYM, DROP ANY SYNONYM, CREATE ANY VIEW, DROP ANY VIEW, CREATE ANY TRIGGER, DROP ANY TRIGGER TO &&OWNER WITH ADMIN OPTION;

GRANT SELECT ON SYS.DBA_CONS_COLUMNS TO &&OWNER WITH GRANT OPTION;

GRANT SELECT ON SYS.DBA_CONSTRAINTS TO &&OWNER WITH GRANT OPTION;

GRANT SELECT ON SYS.DBA_TAB_COLUMNS TO &&OWNER WITH GRANT OPTION;

GRANT SELECT ON SYS.V_$SESSION TO &&OWNER WITH GRANT OPTION;
