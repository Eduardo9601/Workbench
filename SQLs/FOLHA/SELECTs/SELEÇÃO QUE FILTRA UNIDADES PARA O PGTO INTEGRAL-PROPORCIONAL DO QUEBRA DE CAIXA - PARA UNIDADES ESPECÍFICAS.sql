SELECT ORG.COD_CONTRATO
FROM RHFP0310 ORG, RHFP0401 ORG2
  WHERE :DATA_REFERENCIA BETWEEN ORG.DATA_INICIO AND ORG.DATA_FIM
  AND ORG.COD_CONTRATO = SEL.COD_CONTRATO
  AND ORG.COD_ORGANOGRAMA = ORG2.COD_ORGANOGRAMA
  AND ORG2.EDICAO_ORG IN ('016', '158', '190', '198', '202', '205', '224', '228', '252', '254', '269', '277', '278', 
                          '283', '285', '287', '289', '290', '291', '392', '413', '421', '526', '567', '580', '7007')



